function humanizeKey(key: string) {
    const last = key.split(".").pop() || key;
    return last
        .replace(/_/g, " ")
        .replace(/-/g, " ")
        .replace(/\b\w/g, (char) => char.toUpperCase());
}

function applyVariables(value: string, variables?: Record<string, any>) {
    if (!variables) return value;
    return Object.entries(variables).reduce(
        (text, [key, item]) => text.replaceAll(`{${key}}`, String(item)),
        value
    );
}

export const LanguageProvider: React.FC<{ children: any }> = ({ children }) => {
    // Fixed SSR issue with typeof window check
    const [lang, setLang] = useState<Language>(() => {
        if (typeof window !== 'undefined') {
            const saved = localStorage.getItem('bin_language');
            return (saved as Language) || 'en';
        }
        return 'en';
    });

    useEffect(() => {
        localStorage.setItem('bin_language', lang);
        const dir = lang === 'ar' ? 'rtl' : 'ltr';
        document.dir = dir;
        document.documentElement.dir = dir;
        document.body.dir = dir;
        document.documentElement.lang = lang;
        console.info("[BIN i18n]", {
            lang,
            dir: document.documentElement.dir,
            stored: localStorage.getItem("bin_language")
        });
    }, [lang]);

    const t = (key: string, variables?: Record<string, any>) => {
        const direct = translations[lang]?.[key] || translations['en']?.[key];
        return applyVariables(direct || humanizeKey(key), variables);
    };

    const tx = (key: string, fallback: string, variables?: Record<string, any>) => {
        const direct = translations[lang]?.[key] || translations['en']?.[key] || fallback;
        return applyVariables(direct, variables);
    };

    const isRTL = lang === 'ar';

    const toggleLanguage = () => {
        setLang(lang === 'en' ? 'ar' : 'en');
    };

    return (
        <LanguageContext.Provider value={{ lang, language: lang, setLang, toggleLanguage, t, tx, isRTL }}>
            <Box 
                id="bin-app-root"
                dir={isRTL ? 'rtl' : 'ltr'}
                sx={{ 
                    minHeight: '100vh',
                    fontFamily: 'inherit',
                    bgcolor: 'background.default',
                    color: 'text.primary',
                    direction: isRTL ? 'rtl' : 'ltr'
                }}
            >
                {children}
            </Box>
        </LanguageContext.Provider>
    );
};

export const useLanguage = () => {
    const context = useContext(LanguageContext);
    if (!context) throw new Error('useLanguage must be used within LanguageProvider');
    return context;
};
